/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
	uint16_t val;

	if(LL_USART_IsActiveFlag_RXNE(USART2))
	{
		DAT_InAddData(LL_USART_ReceiveData8(USART2),0);
	}
	if(LL_USART_IsActiveFlag_TXE(USART2)  && LL_USART_IsEnabledIT_TXE(USART2))
	{
		val = DAT_OutGetNextData();
		if(val==0xFFFF)
			  LL_USART_DisableIT_TXE(USART2);
		else  LL_USART_TransmitData8(USART2, (uint8_t)val);
	}
	if(LL_USART_IsActiveFlag_ORE(USART2)) val = (uint16_t)LL_USART_ReceiveData8(USART2);

  /* USER CODE END USART2_IRQn 0 */
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */
	uint16_t val;

	if(LL_USART_IsActiveFlag_RXNE(USART3))
	{
		DAT_InAddData2(LL_USART_ReceiveData8(USART3),1);
	}

	if(LL_USART_IsActiveFlag_TXE(USART3)  && LL_USART_IsEnabledIT_TXE(USART3))
	{
		val = DAT_OutGetNextData2();
		if(val==0xFFFF)
			  LL_USART_DisableIT_TXE(USART3);
		else  LL_USART_TransmitData8(USART3, (uint8_t)val);
	}

	if(LL_USART_IsActiveFlag_ORE(USART3)) val = (uint16_t)LL_USART_ReceiveData8(USART3);



  /* USER CODE END USART3_IRQn 0 */
  /* USER CODE BEGIN USART3_IRQn 1 */

  /* USER CODE END USART3_IRQn 1 */
}

/**
  * @brief This function handles TIM5 global interrupt.
  */
void TIM5_IRQHandler(void)
{
  /* USER CODE BEGIN TIM5_IRQn 0 */
static uint8_t div10,div100;
	LL_TIM_ClearFlag_UPDATE(TIM5);
	Timer_1ms++;

	if(!(Timer_1ms&0x07))
		CT_ScanButtons_10ms();

	div10++;
	if(div10>=10)
	{
		Timer_10ms_Flag++;
		div10=0;
		div100++;
		if(Timer_ViewUpdate_10ms) Timer_ViewUpdate_10ms--;
	} else
	if(div100>=10)
	{
		div100=0;
		Timer_100ms++;
		Timer_100ms_Flag++;
		if(!(Timer_100ms&0x07))
			Timer_800ms_Flag=1;
	}

  /* USER CODE END TIM5_IRQn 0 */
  /* USER CODE BEGIN TIM5_IRQn 1 */

  /* USER CODE END TIM5_IRQn 1 */
}

